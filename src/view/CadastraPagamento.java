    private void populaComboBoxObras() {

        ArquivoBinarioObra arqObra = new ArquivoBinarioObra();
        if (diretorioObra == null) {
            JOptionPane.showMessageDialog(this, "Abra o arquivo obra!");
            diretorioObra = arqObra.abrirArquivo();
        }
        ObjectInputStream leitor = arqObra.CriaLeitorBinario(diretorioObra);
        if (leitor != null) {
            listaObras = arqObra.adcLista(leitor);
        }

        for (int ob = 0; ob < listaObras.size(); ob++) {
            jComboBoxObras.addItem(listaObras.get(ob).getNome());
        }

    }

    private void carregaListaPagamento() {
        arquivoBinario = new ArquivoBinarioPagamento();
        if (diretorio == null) {
            diretorio = arquivoBinario.abrirArquivo();

        }
        ObjectInputStream leitor = arquivoBinario.CriaLeitorBinario(diretorio);
        listaPagamentos = arquivoBinario.adcLista(leitor);

        if (!listaPagamentos.isEmpty()) {
            for (int j = 0; j < listaPagamentos.size(); j++) {
                if (listaPagamentos.get(j).getObra().getNome().equals(jComboBoxObras.getSelectedItem().toString())) { //contem a obra do combobox no arquivo de pagamento
                    listaPagObra.add(listaPagamentos.get(j)); //array com pagamento da obra selecionada
                }
            }
        }

        if (listaPagObra.isEmpty()) {
            labelPagamento(true, "Nenhum Pagamento Cadastrado!");
            jButtonNovo.setEnabled(false);
            jButtonEditar.setEnabled(false);
            jButtonExcluir.setEnabled(false);
            jButtonAnterior.setEnabled(false);
            jButtonProximo.setEnabled(false);
        } else {
            jButtonNovo.setEnabled(true);
            jButtonEditar.setEnabled(true);
            jButtonExcluir.setEnabled(true);
            jButtonAnterior.setEnabled(true);
            jButtonProximo.setEnabled(true);
        }
    }

    private void mostraPagamentos(Pagamento pag) {
        System.out.println(pag);
        if (pag != null) {
            jTextFieldValorPag.setText(pag.getValor());
            jSpinnerDia.setValue(pag.getDia());
            jSpinnerMes.setValue(pag.getMes());
            jSpinnerAno.setValue(pag.getAno());

        }
    }

    private Obra obraSelecionada() {
        for (int j = 0; j < listaObras.size(); j++) //pega a obra selecionada
        {
            if (jComboBoxObras.getItemAt(jComboBoxObras.getSelectedIndex()).equals(listaObras.get(j).getNome())) {
                return listaObras.get(j);
            }
        }

        return null;
    }

    private void labelPagamento(boolean show, String txt) {
        jLabelPagamento.setEnabled(show);
        jLabelPagamento.setText(txt);
    }

