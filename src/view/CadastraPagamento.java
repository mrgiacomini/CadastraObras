    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        if (!listaPagObra.isEmpty()) {
            indice++;
        }

        arquivoBinario = new ArquivoBinarioPagamento();
        if (diretorio == null) {

            diretorio = arquivoBinario.abrirArquivo();

        } else {
            ObjectOutputStream escritor = arquivoBinario.CriaEscritorBinario(diretorio, false);

            pag = new Pagamento(jTextFieldValorPag.getText(), obraSelecionada(), (int) jSpinnerDia.getValue(),
                    (int) jSpinnerMes.getValue(), (int) jSpinnerAno.getValue());
            listaPagamentos.add(pag); //adiciona para salvar no arquivo  dps
            listaPagObra.add(pag);  //adiciona para mostrar os pagamentos

            arquivoBinario.EscreveObjeto(escritor, listaPagamentos, true);

        }

        jButtonNovo.setEnabled(true);
        jButtonEditar.setEnabled(true);
        jButtonExcluir.setEnabled(true);
        jButtonAnterior.setEnabled(true);
        jButtonProximo.setEnabled(true);

        JOptionPane.showMessageDialog(this, "O " + (indice + 1) + "º pagamento de " + listaPagObra.get(indice).getObra().getNome() + " foi adicionado com sucesso!");
    }//GEN-LAST:event_jButtonSalvarActionPerformed

    private void jButtonAnteriorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAnteriorActionPerformed
        if (diretorio != null) {

            if (indice > 0) {
                indice--;
                mostraPagamentos(listaPagObra.get(indice));
                labelPagamento(true, (indice + 1) + "º Pagamento");

            } else if (indice == 0) {
                if (!listaPagObra.isEmpty()) {
                    mostraPagamentos(listaPagObra.get(0));
                    labelPagamento(true, (indice + 1) + "º Pagamento");
                }
                JOptionPane.showMessageDialog(this, "Fim da lista!");
            }

        } else {
            carregaListaPagamento();
            if (!listaPagObra.isEmpty()) {
                mostraPagamentos(listaPagObra.get(0));
                labelPagamento(true, (indice + 1) + "º Pagamento");
            }
        }

    }//GEN-LAST:event_jButtonAnteriorActionPerformed

    private void jButtonProximoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonProximoActionPerformed

        if (diretorio != null) {

            if (indice < listaPagObra.size() - 1) {
                indice++;
                mostraPagamentos(listaPagObra.get(indice));
                labelPagamento(true, (indice + 1) + "º Pagamento");
            } else {
                JOptionPane.showMessageDialog(this, "Fim da lista!");
            }

        } else {
            carregaListaPagamento();
            if (!listaPagObra.isEmpty()) {
                mostraPagamentos(listaPagObra.get(0));
                jLabelPagamento.setEnabled(true);
                jLabelPagamento.setText((indice + 1) + "º Pagamento");
            }
        }
    }//GEN-LAST:event_jButtonProximoActionPerformed

