    private void carregaListaCliente() {
        arquivoBinario = new ArquivoBinarioFuncionario();
        if (diretorio == null) {
            diretorio = arquivoBinario.abrirArquivo();
            
        }
        ObjectInputStream leitor = arquivoBinario.CriaLeitorBinario(diretorio);
        listaFuncionarios = arquivoBinario.adcLista(leitor);

    }

    private void mostraClientes(Funcionario func) {
        System.out.println(func);
        if (func != null) {
            jTextFieldNome.setText(func.getNome());
            jTextFieldCPF.setText(func.getCPF());
        }
    }


