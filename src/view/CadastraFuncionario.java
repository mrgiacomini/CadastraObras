    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        if (!listaFuncionarios.isEmpty())
            indice++;
        
        arquivoBinario = new ArquivoBinarioFuncionario();
        if (diretorio == null) {
            diretorio = arquivoBinario.abrirArquivo();
        } else {
            ObjectOutputStream escritor = arquivoBinario.CriaEscritorBinario(diretorio, false);

            func = new Funcionario(jTextFieldNome.getText(), jTextFieldCPF.getText());
            listaFuncionarios.add(func);

            arquivoBinario.EscreveObjeto(escritor, listaFuncionarios, true);
        }
        

        JOptionPane.showMessageDialog(this, "Funcionario " + listaFuncionarios.get(indice).getNome() + " foi adicionado com sucesso!");
    }//GEN-LAST:event_jButtonSalvarActionPerformed

    private void jButtonAnteriorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAnteriorActionPerformed
        if (diretorio != null) {

            if (indice > 0) {
                indice--;
                mostraClientes(listaFuncionarios.get(indice));
            } else if (indice == 0) {
                if (!listaFuncionarios.isEmpty())
                    mostraClientes(listaFuncionarios.get(0));
                JOptionPane.showMessageDialog(this, "Fim da lista!");
            }

        } else {
            carregaListaCliente();
            mostraClientes(listaFuncionarios.get(0));
        }

    }//GEN-LAST:event_jButtonAnteriorActionPerformed

    private void jButtonProximoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonProximoActionPerformed

        if (diretorio != null) {

            if (indice < listaFuncionarios.size() - 1) {
                indice++;
                mostraClientes(listaFuncionarios.get(indice));
            } else {
                JOptionPane.showMessageDialog(this, "Fim da lista!");
            }

        } else {
            carregaListaCliente();
            if (!listaFuncionarios.isEmpty()) {
                mostraClientes(listaFuncionarios.get(0));
            }
        }
    }//GEN-LAST:event_jButtonProximoActionPerformed

    private void jButtonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditarActionPerformed

        arquivoBinario = new ArquivoBinarioFuncionario();
        ObjectOutputStream escritor = arquivoBinario.CriaEscritorBinario(diretorio, false);

        func = new Funcionario(jTextFieldNome.getText(), jTextFieldCPF.getText());
        listaFuncionarios.set(indice, func);

        arquivoBinario.EscreveObjeto(escritor, listaFuncionarios, true);

        JOptionPane.showMessageDialog(this, "Funcionario " + listaFuncionarios.get(indice).getNome() + " foi alterado com sucesso!");
    }//GEN-LAST:event_jButtonEditarActionPerformed

    private void jButtonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirActionPerformed
        
        arquivoBinario = new ArquivoBinarioFuncionario();
        ObjectOutputStream escritor = arquivoBinario.CriaEscritorBinario(diretorio, false);
        
        String nome = listaFuncionarios.get(indice).getNome();
        
        listaFuncionarios.remove(indice);
        
        arquivoBinario.EscreveObjeto(escritor, listaFuncionarios, true);
                
        jTextFieldNome.setText("");
        jTextFieldNome.requestFocus();
        jTextFieldCPF.setText("");
                
        JOptionPane.showMessageDialog(null, "Funcionario " + nome + " foi excluido com sucesso!");
        
        if(listaFuncionarios.isEmpty())
            indice=0;
    }//GEN-LAST:event_jButtonExcluirActionPerformed

